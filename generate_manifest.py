#!/usr/bin/env python3
"""
Run this script from the project root whenever you add a new model run
or regenerate outputs:

    python generate_manifest.py

It scans each model's plots/ and images/ directories and writes manifest.js,
which lets the dashboard work without a live HTTP server.
"""

import os
import json

# Mirror the paths defined in models.js
MODEL_PATHS = [
    "wo_label_smoothing/outputs",
    "outputs_wgan_clean",
    "outputs_wgan_375ep_clean",
    "outputs_wgan_500ep_annealed",
]

manifest = {}
for path in MODEL_PATHS:
    entry = {}
    for subdir in ("plots", "images"):
        full = os.path.join(path, subdir)
        if os.path.isdir(full):
            files = sorted(
                f for f in os.listdir(full) if f.lower().endswith(".png")
            )
            entry[subdir] = files
        else:
            entry[subdir] = []
    manifest[path] = entry

out = "// Auto-generated by generate_manifest.py — do not edit by hand.\n"
out += "const MANIFEST = " + json.dumps(manifest, indent=2) + ";\n"

with open("manifest.js", "w") as f:
    f.write(out)

total = sum(len(v.get("images", [])) + len(v.get("plots", [])) for v in manifest.values())
print(f"manifest.js written — {len(manifest)} models, {total} files indexed.")
